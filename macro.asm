;;;;;;;;;;;;;; PRINTS TEXT ON THE SCREEN WITH ITS CLEARENCE
write MACRO t, c 		
  call clear_screen
  lea dx, t + c
  mov ah, 09h
  int 21h
endm

;;;;;;;;;;;;;; PRINTS TEXT ON THE SCREEN WITHOUT ITS CLEARENCE
write2 MACRO t, c
  lea dx, t + c
  mov ah, 09h
  int 21h
endm
